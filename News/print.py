# sep parametresi kullanılarak birden fazla veri yazdırılabilir
print("Merhaba", "Dünya!", sep="-")

# sep parametresi değeri boşluk olmadığında veriler arasındaki ayracı belirleyebilirsiniz
print("Merhaba", "Dünya!", sep=",")

# sep parametresi belirtilmezse varsayılan olarak boşluk kullanılır
print("Merhaba", "Dünya!")

print("Merhaba", "Dünya!", "ben muslu", sep="sepsep", end=".\n")

print("Merhaba", "Dünya!", "muslu", end=".\n\n\n\n")

